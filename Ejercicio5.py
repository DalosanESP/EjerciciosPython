#Contar cuantas veces se repite una cadena de caracteres en un string
frase = input('Escriba una frase :')
cadena = "aaa"
frase = frase.lower() #para pasar una cadena de string a minisculas (.upper si la queremos en mayusculas)
print ('Numero de veces que se repite la cadena: ',frase.count(cadena))

